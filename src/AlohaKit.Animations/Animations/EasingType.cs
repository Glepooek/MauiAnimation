namespace AlohaKit.Animations
{
    /// <summary>
    /// The EasingType enum provides a set of predefined easing functions used in animations. 
    /// Each easing type determines the progression and behavior of an animation over time, allowing you to create smooth and visually appealing transitions.
    /// </summary>
    public enum EasingType
    {
        BounceIn,
        BounceOut,
        CubicIn,
        CubicInOut,
        CubicOut,
        Linear,
        SinIn,
        SinInOut,
        SinOut,
        SpringIn,
        SpringOut
    }
}
